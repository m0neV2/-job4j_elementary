package ru.job4j.math;

public class MathFunction {
    public static  double sum(double one, double second) {
        return one + second;
    }
    public static double multiple(double one, double second) {
        return one * second;
    }
    public static double minus(double one, double second) {
        return one - second;
    }
    public static double delenie(double one, double second){
        return one / second;
    }
}
